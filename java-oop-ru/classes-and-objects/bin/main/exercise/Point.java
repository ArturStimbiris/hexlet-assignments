package exercise;

public class Point {
    private int coordX;
    private int coordY;

    public Point(int coordX, int coordY) {
        this.coordX = coordX;
        this.coordY = coordY;
    }

    public int getX() {
        return this.coordX;
    }

    public int getY() {
        return this.coordY;
    }
}

